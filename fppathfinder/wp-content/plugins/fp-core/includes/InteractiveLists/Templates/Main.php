/**
 * Main
 *
 * @author Jason Witt
 * @since  1.0.0
 */
class Main {

	/**
	 * Initialize the class
	 *
	 * @author Jason Witt
	 * @since  1.0.0
	 *
	 * @return void
	 */
	public function __construct() {
		$this->init();
	}

	/**
	 * Init
	 *
	 * @author Jason Witt
	 * @since  1.0.0
	 *
	 * @return void
	 */
	public function init() {
		new LoadWpTemplates();
		new ResourceLinks\Template();
		new ShareLink\Template();
		new ShareLink\Messages();
		new ShareLink\Ajax();
		new Flowchart\Enqueue();
	}
}
